#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	if (!lst || !f)
		return ;
	tmp = lst;
	f((void *)tmp);
	if (tmp->next)
	{
		tmp = tmp->next;
		ft_lstiter(tmp, f);
	}
}
