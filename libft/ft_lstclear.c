#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	tmp = *lst;
	if (tmp->next)
	{
		tmp = tmp->next;
		ft_lstdelone(tmp, del);
	}
	free(tmp);
	*lst = NULL;
}
